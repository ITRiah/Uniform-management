    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá sản phẩm không? ", "Xác Nhận", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        dongChon = tblSanPham.getSelectedRow();
        if (dongChon != -1) {
            listSanPham.remove(dongChon);
            updateFile();
            loadTableSanPham();
        } else
            JOptionPane.showMessageDialog(this,
                    "Chưa chọn dòng xóa", "Thông báo", WIDTH);
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        dongChon = tblSanPham.getSelectedRow();
        String msg = "";
        if (dongChon != -1) {
            int choice = JOptionPane.showConfirmDialog(this, "Xác nhận cập nhật phẩm? ", "Xác Nhận", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.NO_OPTION) {
                return;
            }
            SanPham sp = listSanPham.get(dongChon);
            try {
                sp.setTenSp(txtTenSp.getText());
            } catch (Exception ex) {
                msg += ex.getMessage();
            }
            try {
                sp.setSoLuongCon(Integer.parseInt(txtSoLuongCon.getText()));
            } catch (Exception ex) {
                msg += ex.getMessage();
            }
            try {
                sp.setSize(txtSize.getText());
            } catch (Exception ex) {
                msg += ex.getMessage();
            }
            try {
                sp.setDonGia(Double.parseDouble(txtDonGia.getText()));
            } catch (Exception ex) {
                msg += ex.getMessage();
            }

            if (msg.equals("")) {
                listSanPham.set(dongChon, sp);
                loadTableSanPham();
                updateFile();
            } else {
                JOptionPane.showMessageDialog(rootPane, msg, "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else
            JOptionPane.showMessageDialog(this,
                    "Chưa chọn dòng xóa", "Thông báo", WIDTH);
    }//GEN-LAST:event_btnCapNhatActionPerformed

