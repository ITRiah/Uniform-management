    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        String tenSp = txtTenSp.getText();
        String size = txtSize.getText();
        String donGia = txtDonGia.getText();
        String soLuong = txtSoLuongCon.getText();
        String msg = "";

        SanPham sp = new SanPham();
        try {
            sp.setTenSp(txtTenSp.getText());
        } catch (Exception ex) {
            msg += ex.getMessage();
        }
        try {
            sp.setSoLuongCon(Integer.parseInt(txtSoLuongCon.getText()));
        } catch (Exception ex) {
            msg += ex.getMessage();
        }
        try {
            sp.setSize(txtSize.getText());
        } catch (Exception ex) {
            msg += ex.getMessage();
        }
        try {
            sp.setDonGia(Double.parseDouble(txtDonGia.getText()));
        } catch (Exception ex) {
            msg += ex.getMessage();
        }
        
        if(msg.equals("")){
            listSanPham.add(sp);
            loadTableSanPham();
            saveToFile(sp);
        }
        else
            JOptionPane.showMessageDialog(rootPane, msg, "Thông báo", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnThemMoiActionPerformed

