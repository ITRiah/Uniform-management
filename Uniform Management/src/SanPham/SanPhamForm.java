    private void saveToFile(SanPham sanpham) {
        try {
            FileWriter fw = new FileWriter("CSDL\\SanPham.txt", true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(sanpham.toString());
            bw.newLine();
            bw.close();
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    // Method to update the entire file based on the current list of students
    private void updateFile() {
        try {
            FileWriter fw = new FileWriter("CSDL\\SanPham.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            for (SanPham sanpham : listSanPham) {
                bw.write(sanpham.toString());
                bw.newLine();
            }
            bw.close();
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private boolean dieukienSL(String soLuong) {

        try {
            int value = Integer.parseInt(soLuong);
            if (value < 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0 !", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là một số !", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

